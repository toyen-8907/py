import numpy as np

A = np.array([[2,2],[2,2],[2,2]])
B = np.array([[1,4],[2,5],[3,6]])
addAB = A + B
subAB = A - B
mulAB = A * B
divAB = A / B
raiAB = A % B
tA = A.T
tB = B.T
print("A   = \n", A)
print("B   = \n", B)
print("A+B = \n", addAB)
print("A-B = \n", subAB)
print("A*B = \n", mulAB)
print("A/B = \n", divAB)
print("A%B = \n", raiAB)
print("A轉置 =\n", tA)
print("B轉置 =\n", tB)













